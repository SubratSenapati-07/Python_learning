#True or false = Truth Table
#True and False = Truth Table
'''
1. and 
2. or 
3. not 
'''
a,b,c = 1,2,3

# and = all condition are true.Then executed
if(b>a and b>c):  
    print ("true")
else:
    print("false")

# or = atleast 1 condition is true.Then executed
if(b>a or b>c):  
    print ("true")
else:
    print("false")
